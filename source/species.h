int pokemon_index_to_dex(int index) {
	if (index <= 251) // Gen1-2
		return index;
	else if (index <= 276) // Filler "?" species
		return 0;
	else if (index <= 300) // Early Gen3
		return index = 25;
	else if (index <= 412)
		return idx301_to_dex[index - 301];
	else if (index <= 439)
		return 201;
	else
		return 0;
}

